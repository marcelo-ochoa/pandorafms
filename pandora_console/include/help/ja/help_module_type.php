<h1>モジュールの種類</h1>

モジュールの種類一覧:
<ul>
    <li>async_data: Asynchronous numeric data / 非同期数値データ</li>
    <li>async_inc: Asynchronous incremental data / 非同期インクリメンタルデータ</li>
    <li>async_proc: Asynchronous proc data / 非同期booleanデータ</li>
    <li>async_string: Asynchronous string data / 非同期文字列データ</li>
    <li>generic_data: Generic module to acquire numeric data / 数値データ取得の一般モジュール</li>
    <li>generic_data_inc: Generic module to acquire numeric incremental data / インクリメンタルデータ取得の一般モジュール</li>
    <li>generic_data_string: Generic module to acquire alphanumeric data / 文字列取得の一般モジュール</li>
    <li>generic_proc: Generic module to acquire boolean data / booleanデータ取得の一般モジュール</li>
    <li>image_jpg: Image JPG data / JPG画像データ</li>
    <li>image_png: Image PNG data / PNG画像データ</li>
    <li>keep_alive: KeepAlive</li>
    <li>remote_icmp: Remote ICMP network agent (latency) / リモート ICMP エージェント(レガシィ)</li>
    <li>remote_icmp_proc: Remote ICMP network agent, boolean data / リモート ICMP エージェント(booleanデータ)</li>
    <li>remote_snmp: Remote SNMP network agent, numeric data / リモート SNMP エージェント(数値データ)</li>
    <li>remote_snmp_inc: Remote SNMP network agent, incremental data / リモート SNMP エージェント(インクリメンタルデータ)</li>
    <li>remote_snmp_proc: Remote SNMP network agent, boolean data / リモート SNMP エージェント(booleanデータ)</li>
    <li>remote_snmp_string: Remote SNMP network agent, alphanumeric data / リモート SNMP エージェント(文字列データ)</li>
    <li>remote_tcp: Remote TCP network agent, numeric data / リモート TCP エージェント(数値データ)</li>
    <li>remote_tcp_inc: Remote TCP network agent, incremental data / リモート TCP エージェント(インクリメンタルデータ)</li>
    <li>remote_tcp_proc: Remote TCP network agent, boolean data / リモート TCP エージェント(booleanデータ)</li>
    <li>remote_tcp_string: Remote TCP network agent, alphanumeric data / リモート TCP エージェント(文字列データ)</li>
</ul>
