<h1>ポリシーエージェント</h1>

<p>ここではエージェントをポリシーに割り当てることができます。以下の 2つの方法があります。</p>
<ul>
<li type="circle">一括操作</li>
<li type="circle">単一操作</li>
</ul>
<p>ポリシーに追加する複数のエージェントを選択することができます。また、左側のカラムに移動して削除することもできます。</p>
<p>この画面の下の方には、ポリシーに関連づけられたエージェントの一覧および、削除がペンディング状態のエージェントが表示されています。</p>
