<h1>レポートのアイテム一覧</h1>

<p>このタブでは、レポートを作成する全アイテムの全体表示をすることができます。一覧にあるアイテムは、レポート内に順番に表示されます。このタブでできることは以下の通りです。</p>

<ul>
<li type="circle">アイテムの編集 (スパナアイコンをクリック)</li>
<li type="circle">白い矢印のヘッダーセルにて、アルファベットやエージェント名での分類による自動的な表示順の入れ替え</li>
<li type="circle">左側の緑色の矢印での手動での順番の入れ替え</li>
<li type="circle">赤い×印をクリックすることによるアイテムの削除</li>
</ul>
